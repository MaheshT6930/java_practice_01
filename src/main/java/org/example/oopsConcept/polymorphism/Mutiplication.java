package org.example.oopsConcept.polymorphism;

public class Mutiplication {
    public int multiplication (int x, int y){
        return x*y;
    }
    public int multiply(int x, int y, int z){
        return x*y;
    }
    public double multiply(double x, int y, int z){
        return  x*y*z;
    }
    public float multiply_float(float x, float y){
        return x*y;
    }
}
